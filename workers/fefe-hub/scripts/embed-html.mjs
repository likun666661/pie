// Embed the shared viewer HTML (single source of truth lives next to the Rust web UI)
// into a generated TS module so both `wrangler` bundling and the tsc test build can
// import it without custom loader rules.
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const source = join(here, "..", "..", "..", "crates", "coding-agent", "src", "ui", "web_index.html");
const target = join(here, "..", "src", "viewer_html.generated.ts");

const html = readFileSync(source, "utf8");
const banner = "// GENERATED by scripts/embed-html.mjs from crates/coding-agent/src/ui/web_index.html — do not edit.\n";
writeFileSync(target, `${banner}export const VIEWER_HTML: string = ${JSON.stringify(html)};\n`);
console.log(`embedded ${source} (${html.length} bytes)`);
